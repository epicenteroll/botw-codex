// gen-location-types-sql.js — emit the location_types seed INSERTs from the
// single source of truth (src/lib/locationTypes.js), so the Supabase table and
// the JS constant never drift. Run: `npm run gen:sql` → prints SQL to stdout,
// or writes db/seed_location_types.sql.
import { writeFileSync } from 'fs'
import { fileURLToPath } from 'url'
import { dirname, resolve } from 'path'
import { LOCATION_TYPES } from '../src/lib/features/atlas/lib/locationTypes.js'

const esc = (v) => String(v).replace(/'/g, "''")
const rows = LOCATION_TYPES.map(
  (t, i) =>
    `  ('${esc(t.id)}', '${esc(t.display_name)}', '${esc(t.category)}', '${esc(t.marker_colour)}', '#icon-${esc(t.id)}', '', ${i})`,
)

const sql = `-- Auto-generated by scripts/gen-location-types-sql.js — do not edit by hand.
-- ${LOCATION_TYPES.length} location types (Section 11). Re-run \`npm run gen:sql\` after edits.
INSERT INTO public.location_types (id, display_name, category, marker_colour, icon_ref, description, sort_order) VALUES
${rows.join(',\n')}
ON CONFLICT (id) DO UPDATE SET
  display_name = EXCLUDED.display_name,
  category     = EXCLUDED.category,
  marker_colour= EXCLUDED.marker_colour,
  icon_ref     = EXCLUDED.icon_ref,
  sort_order   = EXCLUDED.sort_order;
`

const __dirname = dirname(fileURLToPath(import.meta.url))
const out = resolve(__dirname, '../db/seed_location_types.sql')
writeFileSync(out, sql)
console.log(sql)
console.error(`\n→ wrote ${out} (${LOCATION_TYPES.length} types)`)
