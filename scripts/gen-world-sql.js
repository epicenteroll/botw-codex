// gen-world-sql.js — emit the seeded starter world (the Q2 content) as SQL for
// Supabase. Slugs in seed.json are converted to DETERMINISTIC UUIDs (sha1-based,
// v5-style) so parent_id / origin_entity_id foreign keys resolve and re-runs are
// idempotent. Run: `node scripts/gen-world-sql.js`.
import { readFileSync, writeFileSync } from 'fs'
import { fileURLToPath } from 'url'
import { dirname, resolve } from 'path'
import { createHash } from 'crypto'

const __dirname = dirname(fileURLToPath(import.meta.url))
const seed = JSON.parse(readFileSync(resolve(__dirname, '../src/lib/features/atlas/lib/seed.json'), 'utf8'))

// deterministic UUID from a slug (sha1 → RFC-4122-shaped v5-ish)
function uuid(slug) {
  const h = createHash('sha1').update('botw:' + slug).digest('hex')
  return `${h.slice(0, 8)}-${h.slice(8, 12)}-5${h.slice(13, 16)}-8${h.slice(17, 20)}-${h.slice(20, 32)}`
}
const q = (v) => (v == null ? 'NULL' : `'${String(v).replace(/'/g, "''")}'`)
const num = (v) => (v == null || v === '' ? 'NULL' : Number(v))
const bool = (v) => (v ? 'true' : 'false')
const arr = (a) =>
  !a || !a.length ? `'{}'` : `ARRAY[${a.map((t) => `'${String(t).replace(/'/g, "''")}'`).join(',')}]::text[]`

const ENT_COLS = [
  'id', 'entity_type', 'name', 'parent_id', 'rumour', 'common_knowledge',
  'uncommon_knowledge', 'rare_knowledge', 'gm_lore', 'admin_notes',
  'location_subtype', 'location_scale', 'map_layer', 'coord_x', 'coord_y',
  'sequence_index', 'sector_count', 'power_level', 'water_level', 'tags', 'is_published',
  // Stage 1.5 (D0/H0): persisted blob geometry, reroll seed, charting state, ordering
  'blob_cx', 'blob_cy', 'blob_r', 'blob_seed', 'chart_status', 'sort_order',
]

// H0 backfill at seed time: a sector with published children is 'charted'.
const chartOf = (e) =>
  e.entity_type !== 'sector'
    ? null
    : e.chart_status ||
      (seed.entities.some((k) => k.parent_id === e.id && k.is_published) ? 'charted' : 'uncharted')

const entRows = seed.entities.map((e) => {
  const vals = [
    q(uuid(e.id)),
    q(e.entity_type),
    q(e.name),
    e.parent_id ? q(uuid(e.parent_id)) : 'NULL',
    q(e.rumour || ''),
    q(e.common_knowledge || ''),
    q(e.uncommon_knowledge || ''),
    q(e.rare_knowledge || ''),
    q(e.gm_lore || ''),
    q(e.admin_notes || ''),
    e.location_subtype ? q(e.location_subtype) : 'NULL',
    q(e.location_scale || 'micro'),
    q(e.map_layer || 'world'),
    num(e.coord_x ?? 0),
    num(e.coord_y ?? 0),
    num(e.sequence_index),
    num(e.sector_count),
    e.power_level ? q(e.power_level) : 'NULL',
    e.water_level ? q(e.water_level) : 'NULL',
    arr(e.tags),
    bool(e.is_published),
    // D0 geometry — blob_center:[x,y] in seed.json -> blob_cx/blob_cy columns
    num(e.blob_center ? e.blob_center[0] : null),
    num(e.blob_center ? e.blob_center[1] : null),
    num(e.blob_r),
    // blob_seed defaults to the entity SLUG (current blobPath() behavior), so
    // shapes survive the slug→UUID id change and any later re-slugging
    e.blob_center ? q(e.blob_seed || e.id) : 'NULL',
    chartOf(e) ? q(chartOf(e)) : 'NULL',
    num(e.sort_order),
  ]
  return `  (${vals.join(', ')})`
})

const deepRows = seed.deepLore.map((d) => {
  const vals = [
    q(uuid(d.id)),
    q(uuid(d.entity_id)),
    q(d.origin_faction),
    d.origin_entity_id ? q(uuid(d.origin_entity_id)) : 'NULL',
    q(d.lore_text),
    q(d.access_note || ''),
    num(d.sort_order ?? 0),
    bool(d.is_published),
  ]
  return `  (${vals.join(', ')})`
})

const sql = `-- Auto-generated by scripts/gen-world-sql.js — the seeded starter world.
-- Slugs are mapped to deterministic UUIDs so foreign keys resolve. Optional:
-- the offline prototype reads seed.json directly; load this only if you are
-- running against Supabase (Section 25c, Option A) and want the starter content.
-- Run db/schema.sql first.

INSERT INTO public.world_encyclopedia
  (${ENT_COLS.join(', ')})
VALUES
${entRows.join(',\n')}
ON CONFLICT (id) DO NOTHING;

INSERT INTO public.deep_lore_entries
  (id, entity_id, origin_faction, origin_entity_id, lore_text, access_note, sort_order, is_published)
VALUES
${deepRows.join(',\n')}
ON CONFLICT (id) DO NOTHING;

-- NOTE: vessel_discoveries are intentionally NOT seeded here — discovery is
-- per real character_vessels row. Use the admin discovery-grant controls (or
-- the Admin tab) once you have created a test vessel (see Section 25d).
`

const out = resolve(__dirname, '../db/seed_world.sql')
writeFileSync(out, sql)
console.error(`→ wrote ${out} (${seed.entities.length} entities, ${seed.deepLore.length} deep-lore entries)`)
